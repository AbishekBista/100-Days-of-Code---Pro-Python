# a simple step-by-step method to solve the hangman challenge


# 1. Start
# 2. Generate random word
# 3. Generate as many blanks as the letters in the word
# 4. Ask user to enter the letter
# 5. Does it match?
#     5. a) Yes -> Fill the missing letter in the blanks
#             i. All blanks filled?
#                  Yes -> Game Over 
#                  No -> Go to step 4
#        b) No -> Lose a life
#             i. All lives lost?
#                 Yes -> Game Over 
#                 No -> Go to step 4